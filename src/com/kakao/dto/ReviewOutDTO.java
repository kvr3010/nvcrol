public class ReviewOutDTO {

private int no;
private String je;
private String title;
private float pyung;
private String nae;
private String name;

public int getNo() {
	return no;
}
public void setNo(int no) {
	this.no = no;
}
public String getJe() {
	return je;
}
public void setJe(String je) {
	this.je = je;
}
public String getTitle() {
	return title;
}
public void setTitle(String title) {
	this.title = title;
}
public float getPyung() {
	return pyung;
}
public void setPyung(float pyung) {
	this.pyung = pyung;
}
public String getNae() {
	return nae;
}
public void setNae(String nae) {
	this.nae = nae;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}



}
