package com.kakao.dto;

public class ReviewDTO {

	private int num;
	private String mv_title;
	private String title;
	private int pyung;
	private String nae;
	private String name;
	
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	
	
	public String getMv_title() {
		return mv_title;
	}
	public void setMv_title(String mv_title) {
		this.mv_title = mv_title;
	}
	
	
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	
	
	public int getPyung() {
		return pyung;
	}
	public void setPyung(int pyung) {
		this.pyung = pyung;
	}
	
	
	public String getNae() {
		return nae;
	}
	public void setNae(String nae) {
		this.nae = nae;
	}
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
	
}
