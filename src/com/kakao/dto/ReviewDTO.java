package com.kakao.dto;

public class ReviewDTO {

	private int no;
	private int mv_num;
	private int mb_num;
	private int pyung;
	private String nae;
	private String title;
	
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public int getMv_num() {
		return mv_num;
	}
	public void setMv_num(int mv_num) {
		this.mv_num = mv_num;
	}
	public int getMb_num() {
		return mb_num;
	}
	public void setMb_num(int mb_num) {
		this.mb_num = mb_num;
	}
	public int getPyung() {
		return pyung;
	}
	public void setPyung(int pyung) {
		this.pyung = pyung;
	}
	public String getNae() {
		return nae;
	}
	public void setNae(String nae) {
		this.nae = nae;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	
	
	
	
}
